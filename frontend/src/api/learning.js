export function getVocabularyList(params) {
  return mockApi.get('/vocabulary').then(response => {
    let words = [...response.data]
    if (params?.language) {
      words = words.filter(w => w.language === params.language)
    }
    if (params?.limit) {
      words = words.slice(0, params.limit)
    }
    return {
      data: {
        code: 200,
        data: words
      }
    }
  })
}

